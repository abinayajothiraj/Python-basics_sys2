#casting -  converting one datatype to another

"""""
here we use constructor functions for casting

i) int()
ii) float()
iii) str()
"""

#int
x=int(1)
y=int(9.7)
z=int("5")

print(x)
print(y)
print(z)

#float

a=float(1)
print(a)
b=float(9.7)
print(b)
c=str("5")
print(c)

#str

d=str(1)
print(d)

f=str(4.5)
